/*utilizar o código "var" e em seguida o nome da variável para criar uma variável, atribuir um valor à uma variável,
atribuir o valor de uma variável à outra. (podemos atribuir o valor de uma variável na mesma linha em que a declaramos)*/

var a;
var b;
a = 7;
