//string é um tipo de dado de caracteres fornecido entre aspas, no caso acima, myName foi uma variável de string declarada com a string "carloto".
let catName = "Oliver";
let carSound = "Meow!";
/*o código let também pode ser utilizado para declarar uma variável, mas ele não permite que voce utilize-o novamente para redeclarar uma variável,
diferentemente do código var. (variáveis criada com o código let ainda permitem que seus valores mudem)*/
const FAV_PET = "Cats"
